// Using Push() method
A = [2, 4, 6, 8];
console.log(A);
A.push(10);
console.log(A);

// Using Pop() method
B = ["apple", "banana", "pineapple"];
console.log(B);
B.pop();
console.log(B);

// Using Shift() method
C = ["Audi", "BMW", "Mercedes"];
console.log(C);
C.shift();
console.log(C);

// Using UnShift() method
D = [1,22,43,22];
console.log(D);
D.unshift(23,65);
console.log(D);

// Using Slice() Method
E = ['PC','CPU','MOUSE',"KEYBOARD"];
console.log(E);
F = E.slice(0,2);
console.log(F);

// Using Concat() Method
var num1 = [11,12,13];
    num2 = [14,15,16];
    num3 = [17,18,19];
console.log(num1,num2,num3);
console.log(num1.concat(num2, num3));

// Using Reverse() Method
G = ['Humayun',"Khan","A.R. Kalsekar"];
console.log(G);
G.reverse();
console.log(G);

// Using Sort() Method
const H=[6,5,4,3,2,1];
console.log(H);
H.sort();
console.log(H);